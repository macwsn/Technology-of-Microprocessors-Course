void messageTask(void *p){
	uint8_t rx_char;
	while(1){
		if(xQueueReceive(loopQueueHandle, &rx_char, 0) == pdTRUE) {
		        xprintf("char in:  %c\n", rx_char);
	    }
		xprintf("Current systime is: %05d\n", (int)xTaskGetTickCount());
		portTickType previousWakeTime = xTaskGetTickCount();
		LD1_ON;
		uint32_t t;
		HAL_RNG_GenerateRandomNumber(&hrng,&t);
		t=(t & 0x000001FF);
		HAL_Delay(t);
		LD1_OFF;

		vTaskDelayUntil(&previousWakeTime, 998);
	}
}

void ledTask() {
	vTaskDelay(3);

	for (;;) {
		uint8_t key;
		key = inkey();
		    if(key > 0){
		    	if(key == 's'){
		    		vTaskSuspend(messageTaskHandler);
		    	}
		    	if(key == 'r'){
		    		vTaskResume(messageTaskHandler);
		    	}
		        if(xQueueSend(loopQueueHandle, &key, 0) != pdTRUE) {
		            xprintf("full\n");
		        }
		    }
		LD2_ON;
		HAL_Delay(3);
		LD2_OFF;
		vTaskDelay(3);
	}
}


